public class Triangle {
    double alphaAngle;
    double betaAngle;
    double gammaAngle;

    public Triangle() {

    }

    public Triangle(double alphaAngle, double betaAngle, double gammaAngle) {
        this.alphaAngle = alphaAngle;
        this.betaAngle = betaAngle;
        this.gammaAngle = gammaAngle;
    }

}